
public class Bolita {
	private int x;
	private int y;
	
	public Bolita(){}
	
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	@Override
	public String toString() {
		return "Bolita [x=" + x + ", y=" + y + "]";
	}

}
